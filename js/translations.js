// Function to set the active language
function setLanguage(languageCode) {
  // Default to English if language code not found
  if (!translations[languageCode]) {
    languageCode = 'en';
  }
  
  // Save the selected language preference
  localStorage.setItem('preferred_language', languageCode);
  
  // Update the language display in the dropdown
  document.querySelector('.language-text').textContent = 
    document.querySelector(`.language-option[data-lang="${languageCode}"]`).textContent.split(' ')[0];
  
  // Get all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  
  // Update text content for each element
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[languageCode][key]) {
      el.textContent = translations[languageCode][key];
    }
  });
  
  // Update placeholders for input elements
  const inputElements = document.querySelectorAll('input[data-i18n-placeholder]');
  inputElements.forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[languageCode][key]) {
      el.placeholder = translations[languageCode][key];
    }
  });
  
  // Update button text
  const buttonElements = document.querySelectorAll('button[data-i18n]');
  buttonElements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[languageCode][key]) {
      // If the button has both an icon and text, preserve the icon
      const iconElement = el.querySelector('i');
      if (iconElement) {
        // Keep the icon and replace only the text
        el.innerHTML = '';
        el.appendChild(iconElement);
        el.innerHTML += ' ' + translations[languageCode][key];
      } else {
        el.textContent = translations[languageCode][key];
      }
    }
  });
  
  // Dispatch a custom event for components that need to know when language changes
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: languageCode } }));
}

// Export the translations and functions
window.farmAssistTranslations = {
  translations,
  setLanguage
};

// Initialize the language from localStorage or default to 'en' when the script loads
document.addEventListener('DOMContentLoaded', function() {
  const savedLanguage = localStorage.getItem('preferred_language') || 'en';
  setLanguage(savedLanguage);
  
  // Add event listeners to language selection options
  const languageOptions = document.querySelectorAll('.language-option');
  languageOptions.forEach(option => {
    option.addEventListener('click', function(e) {
      e.preventDefault();
      const lang = this.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
});